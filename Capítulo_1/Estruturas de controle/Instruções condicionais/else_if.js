// Se tivermos várias expressões, podemos usar if...else diversas vezes para executar blocos de código diferentes

var month = 5;
if (month === 1) {
    console.log("January");
} else if (month === 2) {
    console.log("February");
} else if (month === 3) {
    console.log("March");
} else {
    console.log("Month is not January, February or March");
}
